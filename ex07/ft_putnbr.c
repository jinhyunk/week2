#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, &"-", 1);
		write(1, &"2", 1);
		nb=147483648;
	}
	else if (nb < 0){
		nb = -nb;
		write(1, &"-", 1);
	}
	else{
		if (nb > 9)
		{
			ft_char(nb / 10);
		}
		ft_char(48 + nb % 10);
	}
}
